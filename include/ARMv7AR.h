/* PSR(Program Status Register) Mode Bit Values */
#define ARM_MODE_BIT_USR 0x10 /*1 0000*/
#define ARM_MODE_BIT_FIQ 0x11 /*1 0001*/
#define ARM_MODE_BIT_IRQ 0x12 /*1 0010*/
#define ARM_MODE_BIT_SVC 0x13 /*1 0011*/
#define ARM_MODE_BIT_ABT 0x17 /*1 0111*/
#define ARM_MODE_BIT_UND 0x1B /*1 1011*/
#define ARM_MODE_BIT_SYS 0x1F /*1 1111*/
#define ARM_MODE_BIT_MON 0x16 /*1 0110*/
